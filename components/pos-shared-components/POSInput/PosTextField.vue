<script>
  import { GTextFieldBs } from 'pos-vue-framework';

  export default {
    name: 'PosTextField',
    props: {
      label: null,
      virtualEvent: Boolean
    },
    mixins: [GTextFieldBs],
    mounted() {
      this.$nextTick(() => {
        const input = this.$refs.input
        input.addEventListener('keydown', e => {
          if(e.keyCode === 13) {
            this.$emit('enterpressed', input.value)
          }
        })
      })
    }
  }
</script>

